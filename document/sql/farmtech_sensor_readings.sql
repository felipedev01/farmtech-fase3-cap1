-- Consulta exigida como evidencia basica.
SELECT *
FROM FARMTECH_SENSOR_READINGS
ORDER BY created_at DESC;

-- Ultimas 10 leituras.
SELECT *
FROM FARMTECH_SENSOR_READINGS
ORDER BY created_at DESC
FETCH FIRST 10 ROWS ONLY;

-- Resumo por cultura.
SELECT
    cultura,
    COUNT(*) AS total_leituras,
    ROUND(AVG(umidade_percent), 2) AS umidade_media,
    ROUND(AVG(temperatura_c), 2) AS temperatura_media,
    ROUND(AVG(ph), 2) AS ph_medio,
    SUM(bomba_ligada) AS acionamentos_bomba
FROM FARMTECH_SENSOR_READINGS
GROUP BY cultura
ORDER BY cultura;

-- Leituras em que a bomba deveria atuar por baixa umidade.
SELECT
    id,
    created_at,
    cultura,
    umidade_percent,
    limiar_umidade_percent,
    bomba_ligada
FROM FARMTECH_SENSOR_READINGS
WHERE umidade_percent < limiar_umidade_percent
ORDER BY created_at DESC;
