CREATE TABLE FARMTECH_SENSOR_READINGS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    device_id VARCHAR2(80) NOT NULL,
    cultura VARCHAR2(40) NOT NULL,
    umidade_percent NUMBER(5, 2) NOT NULL,
    temperatura_c NUMBER(5, 2) NOT NULL,
    ph NUMBER(4, 2) NOT NULL,
    n_presente NUMBER(1) DEFAULT 0 NOT NULL,
    p_presente NUMBER(1) DEFAULT 0 NOT NULL,
    k_presente NUMBER(1) DEFAULT 0 NOT NULL,
    limiar_umidade_percent NUMBER(5, 2) NOT NULL,
    bomba_ligada NUMBER(1) DEFAULT 0 NOT NULL,
    CONSTRAINT chk_farmtech_n CHECK (n_presente IN (0, 1)),
    CONSTRAINT chk_farmtech_p CHECK (p_presente IN (0, 1)),
    CONSTRAINT chk_farmtech_k CHECK (k_presente IN (0, 1)),
    CONSTRAINT chk_farmtech_bomba CHECK (bomba_ligada IN (0, 1)),
    CONSTRAINT chk_farmtech_ph CHECK (ph BETWEEN 0 AND 14)
);

CREATE INDEX IDX_FARMTECH_SENSOR_CREATED
    ON FARMTECH_SENSOR_READINGS (created_at DESC);

CREATE INDEX IDX_FARMTECH_SENSOR_CULTURA
    ON FARMTECH_SENSOR_READINGS (cultura);

